#function with return value

def add (a,b):
    print("Adding")
    return a+b
def subtract(a,b):
    print("Subtracing")
    return a-b

def multiply(a,b):
    print("Mutiplying")
    return a*b

def divide(a,b):
    print("Dividing")
    return a/b;

print(add(1,2))
print(subtract(1,2))
print(multiply(1,2))
print(divide(1,2))
